# Variaveis
nome = "Gisele"
idade = 25

# Exibição de valores
print(nome)
print(idade)

# Para dar espaço
print("\n")

# 1 metodo de concatenação (string: str)
print("O meu nome é " + nome + " e tenho " + str(idade) + " anos.\n")

# 2 metodo de concatenação (format)
print("O meu nome é {} e tenho {} anos.\n".format(nome, idade))

# 3 metodo de concatenação (f string) pode usar aspas simples tambem
print(f"O meu nome é {nome} e tenho {idade} anos." )